    private void jBtnConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConfirmarActionPerformed
        // TODO add your handling code here:
        Venda venda = new Venda();
        
        int cod = Integer.parseInt( jTxtCodigo.getText());
        int desc = Integer.parseInt( jFmtDesconto.getText());
        int vtotal = Integer.parseInt( jTxtValorTot.getText());
        int taxa = Integer.parseInt( jFmtTaxa.getText());
        
        venda.setId_venda_eha(cod);
        venda.setFormaPagamento_eha(jTxtFormPag.getText() );
        venda.setLocal_eha(jTxtLocal.getText() );
        venda.setDesconto_eha(desc );
        venda.setValorTotal_eha(vtotal);
        venda.setTaxaEntrega_eha(taxa );
        venda.setStatusEntrega_ldm(jTxtStatus.getText() );
        venda.setDataVenda_eha(null );

        VendaDAO vendaDAO = new VendaDAO();
        vendaDAO.insert(venda);
        
        Util.limpar(jFmtDesconto, 
              jTxtDataVenda  , jTxtFormPag,jTxtLocal,jFmtTaxa , jTxtStatus, jTxtCodigo, jTxtValorTot);
        desabilitar();
    }//GEN-LAST:event_jBtnConfirmarActionPerformed

